
public class Student6 {
	private String name;
	private String sex;
	private int score;

	public Student6(String name, String sex, int score) {
		super();
		this.name = name;
		this.sex = sex;
		this.score = score;
	}

	public String getName() {
		return name;
	}

	public String getSex() {
		return sex;
	}

	public int getScore() {
		return score;
	}
	
	
	
}
